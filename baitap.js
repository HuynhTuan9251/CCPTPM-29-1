function shorten(text, maxLen) {
  text = text || "";
  if (text.length <= maxLen) return text;
  return text.slice(0, maxLen) + "...";
}

// ch·ªëng XSS c∆° b·∫£n khi render
function escapeHtml(str) {
  return String(str ?? "")
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

