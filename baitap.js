function getMaxIdString(list) {
  // list: [{id:"1"},...]
  let max = 0;
  for (const item of list) {
    const n = parseInt(item.id, 10);
    if (!Number.isNaN(n)) max = Math.max(max, n);
  }
  return max;
}

function nextIdString(list) {
  return String(getMaxIdString(list) + 1);
}

