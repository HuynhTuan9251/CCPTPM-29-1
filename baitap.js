function clearPostForm() {
  document.getElementById("postId").value = "";
  document.getElementById("postTitle").value = "";
  document.getElementById("postSlug").value = "";
  document.getElementById("postContent").value = "";
}

function savePost() {
  const db = loadDB();

  const id = document.getElementById("postId").value.trim(); // tr·ªëng khi create
  const title = document.getElementById("postTitle").value.trim();
  const slug = document.getElementById("postSlug").value.trim();
  const content = document.getElementById("postContent").value.trim();

  if (!title) return alert("Title kh√¥ng ƒë∆∞·ª£c ƒë·ªÉ tr·ªëng!");

  if (!id) {
    // CREATE
    const newId = nextIdString(db.posts);
    db.posts.push({
      id: newId,               // ID d·∫°ng chu·ªói
      title,
      slug,
      content,
      isDeleted: false
    });
    saveDB(db);
    alert(`T·∫°o post th√†nh c√¥ng! ID m·ªõi = ${newId}`);
  } else {
    // UPDATE
    const idx = db.posts.findIndex(p => p.id === id);
    if (idx === -1) return alert("Kh√¥ng t√¨m th·∫•y post ƒë·ªÉ s·ª≠a!");
    db.posts[idx].title = title;
    db.posts[idx].slug = slug;
    db.posts[idx].content = content;
    saveDB(db);
    alert("C·∫≠p nh·∫≠t post th√†nh c√¥ng!");
  }

  originalData = loadDB().posts;
  filteredData = structuredClone(originalData);
  renderTable(filteredData);
  clearPostForm();
}

function editPost(id) {
  const db = loadDB();
  const post = db.posts.find(p => p.id === id);
  if (!post) return alert("Kh√¥ng t√¨m th·∫•y post!");

  document.getElementById("postId").value = post.id;
  document.getElementById("postTitle").value = post.title || "";
  document.getElementById("postSlug").value = post.slug || "";
  document.getElementById("postContent").value = post.content || "";
}

function softDeletePost(id) {
  const db = loadDB();
  const post = db.posts.find(p => p.id === id);
  if (!post) return alert("Kh√¥ng t√¨m th·∫•y post!");
  post.isDeleted = true; // ‚úÖ xo√° m·ªÅm
  saveDB(db);

  originalData = loadDB().posts;
  filteredData = structuredClone(originalData);
  renderTable(filteredData);

  // n·∫øu ƒëang ch·ªçn post n√†y, v·∫´n cho xem comment
  const selectedId = document.getElementById("selectedPostId").value;
  if (selectedId === id) renderComments(id);
}

function restorePost(id) {
  const db = loadDB();
  const post = db.posts.find(p => p.id === id);
  if (!post) return alert("Kh√¥ng t√¨m th·∫•y post!");
  post.isDeleted = false;
  saveDB(db);

  originalData = loadDB().posts;
  filteredData = structuredClone(originalData);
  renderTable(filteredData);

  const selectedId = document.getElementById("selectedPostId").value;
  if (selectedId === id) renderComments(id);
}

