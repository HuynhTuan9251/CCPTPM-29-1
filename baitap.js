function renderTable(data) {
  const tableBody = document.getElementById("tableBody");
  tableBody.innerHTML = "";

  if (!data || data.length === 0) {
    tableBody.innerHTML = `<tr><td colspan="6" class="text-center text-danger">Kh√¥ng c√≥ post n√†o!</td></tr>`;
    document.getElementById("totalCount").innerText = 0;
    return;
  }

  data.forEach(post => {
    const deletedClass = post.isDeleted ? "deleted-row" : "";
    const statusBadge = post.isDeleted
      ? `<span class="badge bg-danger">Deleted</span>`
      : `<span class="badge bg-success">Active</span>`;

    const row = document.createElement("tr");
    row.className = deletedClass;

    row.innerHTML = `
      <td><strong>${post.id}</strong></td>
      <td class="pointer" onclick="selectPost('${post.id}')">${escapeHtml(post.title)}</td>
      <td>${escapeHtml(post.slug || "")}</td>
      <td>${escapeHtml(shorten(post.content, 80))}</td>
      <td>${statusBadge}</td>
      <td class="d-flex gap-2 flex-wrap">
        <button class="btn btn-sm btn-outline-primary" onclick="editPost('${post.id}')">‚úèÔ∏è S·ª≠a</button>
        <button class="btn btn-sm btn-outline-danger" onclick="softDeletePost('${post.id}')">üßπ Xo√° m·ªÅm</button>
        <button class="btn btn-sm btn-outline-secondary" onclick="restorePost('${post.id}')">‚ôªÔ∏è Kh√¥i ph·ª•c</button>
      </td>
    `;

    tableBody.appendChild(row);
  });

  document.getElementById("totalCount").innerText = data.length;
}

